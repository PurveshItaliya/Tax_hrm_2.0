import 'package:flutter/material.dart';

heightSpacer(double heightSize) {
  return SizedBox(height: heightSize);
}

widthSpacer(double widthSize) {
  return SizedBox(width: widthSize);
}

comboSpacer(double widthSize, double heightSize) {
  return SizedBox(width: widthSize, height: heightSize);
}
